def calcular_promedio(a, b):
    """
    Calcula el promedio de dos números.
    
    Parámetros:
        a (int/float): Primer número
        b (int/float): Segundo número
    
    Retorno:
        float: Promedio de los dos números ingresados
    """
    return (a + b) / 2
